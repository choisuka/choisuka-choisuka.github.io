    # 더 직접적인 방법: 현재의 깨진 places 배열을 통째로 교체
    # 각 조건의 places 배열은 `places:[{...}]` 형태 (한 줄)
    import re

    # 모든 places:[...] 블록을 순서대로 추출하고 교체
    # 조건 순서: clear, partlyCloudy, cloudy, foggy, drizzle, rainy, snowy, thunderstorm
    order = ['clear', 'partlyCloudy', 'cloudy', 'foggy', 'drizzle', 'rainy', 'snowy', 'thunderstorm']

    # places:[ ... ], 패턴 — 대괄호 안에 중첩 없음
    pattern = re.compile(r'places:\[[^\]]*\]')
    matches = list(pattern.finditer(html))

    if len(matches) != 8:
        print(f"  WARNING: found {len(matches)} places arrays (expected 8)")

    # 뒤에서부터 교체 (인덱스 유지)
    for i in reversed(range(min(len(matches), 8))):
        m = matches[i]
        cond = order[i]
        new_val = 'places:' + PLACES[cond]
        html = html[:m.start()] + new_val + html[m.end():]

    with open(path, 'w', encoding='utf-8') as f:
        f.write(html)
    print(f"Fixed: {path}")

